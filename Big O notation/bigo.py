# O(1) constant
array = [1,2,3,4,5]

def helloWorld(array):
    print(helloWorld)

# O(n) linear
arrayOne = [1,2,3,4,5]

def printArray(arrayOne):
    for number in arrayOne:
        print(number)

# O(n^2) quadratic
arrayTwo = [[1,2,3],[4,5,6],[7,8,9]]

def printArrayTwo(arrayTwo):
    for row in arrayTwo:
        for number in row:
            print(number)



printArrayTwo(arrayTwo)
